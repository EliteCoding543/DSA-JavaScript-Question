// Post Increment and 

var num = 5;
var newNum = num++;
console.log("Post Increment:", num);
console.log("Value after Post Increment:", newNum);

// Post Decrement
var postDec = 10;
var newPostDec = postDec--;
console.log("Post Decrement:", postDec);
console.log("Value after Post Decrement:", newPostDec);

// Pre Decrement 
var anotherNum = 10;
var newAnotherNum = --anotherNum;
console.log("Value after Pre Decrement:", newAnotherNum);
console.log("Pre Decrement:", anotherNum);
